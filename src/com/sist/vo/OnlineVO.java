package com.sist.vo;

/*
 * 임시로 CLASS테이블 참조
 */
public class OnlineVO {
	private String ctitle, cposter, cartist, cprice;
	private int cno;

	public int getCno() {
		return cno;
	}

	public void setCno(int cno) {
		this.cno = cno;
	}

	public String getCtitle() {
		return ctitle;
	}

	public void setCtitle(String ctitle) {
		this.ctitle = ctitle;
	}

	public String getCposter() {
		return cposter;
	}

	public void setCposter(String cposter) {
		this.cposter = cposter;
	}

	public String getCartist() {
		return cartist;
	}

	public void setCartist(String cartist) {
		this.cartist = cartist;
	}

	public String getCprice() {
		return cprice;
	}

	public void setCprice(String cprice) {
		this.cprice = cprice;
	}

}
